# Ler dois números inteiros, executar e mostrar os seguintes operções: adição, subtração, mutiplicação e divisão

n1 = float(input('Digite um número: '))
n2 = float(input('Digite outro número: '))
print(f'A adição {n1+n2}')
print(f'A subtração {n1-n2}')
print(f'A multiplicação {n1*n2}')
print(f'A divição {n1 / n2}')